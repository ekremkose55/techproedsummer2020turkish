package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		// int data type de iki variable olustur. birinciyi 3 ile 2. yi 7 artir
		 
		int sayi1 = 12;
		System.out.println(sayi1);
		sayi1 = sayi1 + 3; //
		System.out.println(sayi1);
		
		int sayi2 = 20;
		System.out.println(sayi2);
		sayi2 = sayi2 + 7;
		System.out.println(sayi2);
		

	}

}
