package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// int data tipinde iki variable olusturun
		// bu variable lari en kisa yolu kullanarak 1 artirin
		// en kisa yol sadece +1 icindir
		
		int sayi1 = 12;
		sayi1++;     // variable den sonra ++ koymak bir artirmaktir
		System.out.println(sayi1);

		int sayi2 = 54;
		sayi2++;
		System.out.println(sayi2);
		
		
		
		
	}

}
