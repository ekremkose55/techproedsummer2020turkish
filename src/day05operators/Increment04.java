package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		// int data da bir variable olustur 
		// bu variable yi her seferinde bir artirmak icin 3 yontemi kullan
		
		int num1 =52;
		num1 = num1 + 1; // 1. yol
		num1+=1;         // 2. yol
		num1++;          // 3. yol
		System.out.println(num1);
		
		
		
		
		
		
	}

}
