		 System.out.print(harf1);// ab seklinde yazdirir
		 System.out.println(harf2);
		 
        // bir char in ascii degeri tablo olmadan nasil bulunur
		// 'a' nin ascii degerini bulmak icin
		 int charDegera = 'a';
		 System.out.println(charDegera);
		 
		 // ! % * sayi degerlerini bulunuz
		 int unlem = '!';
