package homeworkday05;

public class Decrement01 {

	public static void main(String[] args) {
		// int data type'inda bir variable olusturun.
        // Bu variable'in degerini 3 yontemi de kullanarak her defasinda 1 azaltin.
		
		int sayi1 = 12;
		sayi1 = sayi1 - 1;
		System.out.println(sayi1);//uzun yol
		
		sayi1-=1;
		System.out.println(sayi1);// kisa yol
		
		sayi1--;
		System.out.println(sayi1);// en kisa yol
		
		
		

	}

}
