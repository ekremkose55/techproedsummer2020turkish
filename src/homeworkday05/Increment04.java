package homeworkday05;

public class Increment04 {

	public static void main(String[] args) {
		// int data da bir variable olustur 
		// bu variable yi her seferinde bir artirmak icin 3 yontemi kullan
		
		int sayi3 = 12;
		sayi3 = sayi3 + 1;
		sayi3+=1;
		sayi3++;
		System.out.println(sayi3);//15
		
		int num = 97;
		num = num + 1;
		num+=1;
		num++;
		System.out.println(num);//99
		

	}

}
