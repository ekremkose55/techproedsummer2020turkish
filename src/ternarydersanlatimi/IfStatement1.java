	public static void main(String[] args) {
		// Bir integer variable olusturun ve deger atayin
        // Deger 0'dan buyukse ise ekrana "pozitif sayi" yazdirin 
		// Deger 0'dan kucukse ise ekrana "negatif sayi" yazdirin 
		// Deger 0 ise ise ekrana "notr sayi" yazdirin 
		
		int num = 0;
		
		if (num>0) {
			System.out.println("pozitif sayi");
		}
		if(num<0) {
			System.out.println("negatif sayi");
		}
		if(num==0) {
			System.out.println("notr sayi");
		}
		
		
		
		
	}

